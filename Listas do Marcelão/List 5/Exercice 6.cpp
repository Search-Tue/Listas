#include <iostream>
using namespace std;

void verificaLetra(char c) {
   switch(c) {
      case 'A':
      case 'B':
      case 'C':
      case 'D':
      case 'E':
      case 'F':
      case 'G':
      case 'H':
      case 'I':
      case 'J':
      case 'K':
      case 'L':
      case 'M':
      case 'N':
      case 'O':
      case 'P':
      case 'Q':
      case 'R':
      case 'S':
      case 'T':
      case 'U':
      case 'V':
      case 'W':
      case 'X':
      case 'Y':
      case 'Z':
         cout << "A letra " << c << " e maiuscula." << endl;
         break;

      case 'a':
      case 'b':
      case 'c':
      case 'd':
      case 'e':
      case 'f':
      case 'g':
      case 'h':
      case 'i':
      case 'j':
      case 'k':
      case 'l':
      case 'm':
      case 'n':
      case 'o':
      case 'p':
      case 'q':
      case 'r':
      case 's':
      case 't':
      case 'u':
      case 'v':
      case 'w':
      case 'x':
      case 'y':
      case 'z':
         cout << "A letra " << c << " e minuscula." << endl;
         break;

      default:
         cout << c << " nao e uma letra." << endl;
         break;
   }
}

int main() {
   char c;

   cout << "Digite um caractere: ";
   cin >> c;

   verificaLetra(c);

   return 0;
}

